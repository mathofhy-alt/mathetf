export function subjectFor(grade: string, sem: string, curriculum: string = '2022'): string {
    if (curriculum === '2015') {
        if (grade === '고1') return sem === '1' ? '수학(상)' : '수학(하)';
        if (grade === '고2') return sem === '1' ? '수학I' : '수학II';
        if (grade === '고3') return '미적분';
        return '수학(상)';
    }
    if (grade === '고1') return sem === '1' ? '공통수학1' : '공통수학2';
    if (grade === '고2') return sem === '1' ? '대수' : '미적분I';
    if (grade === '고3') return sem === '1' ? '확률과통계' : '미적분II';
    return '공통수학1';
}

